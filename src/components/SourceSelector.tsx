export default function SourceSelector({ sources, currentIndex, loading, onSwitch }: SourceSelectorProps) {
  if (sources.length < 1) return null;

  return (
    <div className="border-t border-zinc-800/40 bg-black">
      <div className="max-w-7xl mx-auto px-4 py-8">
        <div className="flex items-baseline gap-3 mb-2">
          <h2 className="text-lg font-bold text-white tracking-tight">Servers</h2>
        </div>
        <p className="text-xs text-zinc-500 mb-5">🚀 Try another server if one isn&apos;t working.</p>

        <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-4 gap-3">
          {sources.map((s, i) => {
            const selected = i === currentIndex;
            return (
              <button
                key={s.id}
                onClick={() => onSwitch(i)}
                className={`
                  flex items-center gap-3 px-4 rounded-xl border transition-all duration-200 h-[54px]
                  ${selected
                    ? 'bg-[#1f1f1f] border-red-600 shadow-[0_0_15px_rgba(220,38,38,0.25)] font-bold text-white'
                    : 'bg-[#1a1a1a] border-zinc-800 text-zinc-400 hover:bg-[#222] hover:border-zinc-600 hover:-translate-y-0.5 hover:text-zinc-200'
                  }
                `}
              >
                <span className={`${selected ? 'text-red-400' : 'text-zinc-500'}`}>
                  {ICONS[i % ICONS.length]}
                </span>
                <span className="text-sm truncate">{s.name}</span>
                {selected && loading && (
                  <span className="ml-auto w-2 h-2 bg-red-500 rounded-full animate-pulse shrink-0" />
                )}
              </button>
            );
          })}
        </div>
      </div>
    </div>
  );
}
